#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p_s;

	i = 0;
	p_s = (unsigned char *)s;
	while (i < n && p_s[i] != (unsigned char)c)
		i++;
	if (i == n)
		return (0);
	return ((void *)(p_s + i));
}
